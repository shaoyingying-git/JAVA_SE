public class Demo2 {
    public static void main(String[] args) {
        // 字符串常量
        System.out.println("字符串常量");
        // 字符常量
        System.out.println('a');
        // 整数常量
        System.out.println(100);
        // 小数常量
        System.out.println(12.3);
        // 布尔常量  true/false
        System.out.println(true);
        System.out.println(false);
        // 空常量  暂时打印不出来
    }
}
