public class Demo4 {
    public static void main(String[] args) {
        // 定义不同类型的变量
//        byte
        byte b = 10;
        System.out.println(b);

//        short
        short s = 20;
        System.out.println(s);

//        int
        int i = 30;
        System.out.println(i);

//        long
        long l = 2222;
        System.out.println(l);

//        float 注意:加上f(F)
//        float f = 12.3;  // 出问题了: 12.3默认是double, 将double赋值给float类型
        float f = 12.3f;
        System.out.println(f);

//        double
        double d = 23.4;
        System.out.println(d);

//        char
        char c = 'a';
        System.out.println(c);

//        boolean
        boolean boo = true;
        System.out.println(boo);
    }
}
