public class Demo3 {
    public static void main(String[] args) {

        // 第一种定义格式:
        // 数据类型  变量名  =  值;
        // 作用: 定义某种数据类型的变量, 将等号右边的值, 赋值给等号左边的变量
        int a = 10;

        // 第二种定义格式
        // 数据类型  变量名;
        // 变量名  =  值;
        int b;
        b = 20;

        // 注意不要同时声明两个变量名相同的变量
    }
}
