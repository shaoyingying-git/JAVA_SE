package com.itheima._02face_object.test01;

/**
 * 这是一个人类
 */
public class Person {
    // 属性 -> 成员变量
    // 姓名
    String name;
    // 年龄
    int age;

    // 行为
    // 吃饭
    public void eat() {
        System.out.println("人要吃饭!~");
    }

    // 睡觉
    public void sleep() {
        System.out.println("人要睡觉!~");
    }

}
