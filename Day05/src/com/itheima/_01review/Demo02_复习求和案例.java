package com.itheima._01review;

public class Demo02_复习求和案例 {
    public static void main(String[] args) {
        // int sum = getSum(234, 432);
        System.out.println(getSum(234, 432));
    }

    /*
        此方法用于求两个数的和
        返回值类型: 和 int
        参数列表: 两个数 int a, int b
     */
    public static int getSum(int a, int b) {
        // 返回a加b的结果
        return a + b;
    }
}
